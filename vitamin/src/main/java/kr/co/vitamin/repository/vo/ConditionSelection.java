package kr.co.vitamin.repository.vo;

public class ConditionSelection {
	private int conditionNo;
	private String conditionType;
	private String conditionTypeName;
	private String conditionSection;
	private String conditionSectionName;
	public int getConditionNo() {
		return conditionNo;
	}
	public void setConditionNo(int conditionNo) {
		this.conditionNo = conditionNo;
	}
	public String getConditionType() {
		return conditionType;
	}
	public void setConditionType(String conditionType) {
		this.conditionType = conditionType;
	}
	public String getConditionTypeName() {
		return conditionTypeName;
	}
	public void setConditionTypeName(String conditionTypeName) {
		this.conditionTypeName = conditionTypeName;
	}
	public String getConditionSection() {
		return conditionSection;
	}
	public void setConditionSection(String conditionSection) {
		this.conditionSection = conditionSection;
	}
	public String getConditionSectionName() {
		return conditionSectionName;
	}
	public void setConditionSectionName(String conditionSectionName) {
		this.conditionSectionName = conditionSectionName;
	}
	
	
}
