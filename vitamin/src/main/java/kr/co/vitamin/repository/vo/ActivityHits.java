package kr.co.vitamin.repository.vo;

public class ActivityHits {
	private int hits_no;
	private int activity_no;
	private int company_no;
	private String company_name;
	
	public int getHits_no() {return hits_no;}
	public void setHits_no(int hits_no) {this.hits_no = hits_no;}
	
	public int getActivity_no() {return activity_no;}
	public void setActivity_no(int activity_no) {this.activity_no = activity_no;}
	
	public int getCompany_no() {return company_no;}
	public void setCompany_no(int company_no) {this.company_no = company_no;}
	
	public String getCompany_name() {return company_name;}
	public void setCompany_name(String company_name) {this.company_name = company_name;}
	
	
}
