package kr.co.vitamin.repository.vo;

public class Hope {
	
	private Integer hopeNo, cityCode, areaCode;
	private String hopeWork,hopeSalary;
	public Integer getHopeNo() {
		return hopeNo;
	}
	public void setHopeNo(Integer hopeNo) {
		this.hopeNo = hopeNo;
	}
	public Integer getCityCode() {
		return cityCode;
	}
	public void setCityCode(Integer cityCode) {
		this.cityCode = cityCode;
	}
	public Integer getAreaCode() {
		return areaCode;
	}
	public void setAreaCode(Integer areaCode) {
		this.areaCode = areaCode;
	}
	public String getHopeWork() {
		return hopeWork;
	}
	public void setHopeWork(String hopeWork) {
		this.hopeWork = hopeWork;
	}
	public String getHopeSalary() {
		return hopeSalary;
	}
	public void setHopeSalary(String hopeSalary) {
		this.hopeSalary = hopeSalary;
	}

	
	
}
