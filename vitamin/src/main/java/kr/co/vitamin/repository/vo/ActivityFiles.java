package kr.co.vitamin.repository.vo;

public class ActivityFiles {
	private int at_file_no;
	private int at_item_no;
	private String at_file_name;
	private String at_file_origin;
	private String at_file_path;
	
	public int getAt_file_no() {return at_file_no;}
	public void setAt_file_no(int at_file_no) {this.at_file_no = at_file_no;}
	
	public int getAt_item_no() {return at_item_no;}
	public void setAt_item_no(int at_item_no) {this.at_item_no = at_item_no;}
	
	public String getAt_file_name() {return at_file_name;}
	public void setAt_file_name(String at_file_name) {this.at_file_name = at_file_name;}
	
	public String getAt_file_origin() {return at_file_origin;}
	public void setAt_file_origin(String at_file_origin) {this.at_file_origin = at_file_origin;}
	
	public String getAt_file_path() {return at_file_path;}
	public void setAt_file_path(String at_file_path) {this.at_file_path = at_file_path;}
	
}
