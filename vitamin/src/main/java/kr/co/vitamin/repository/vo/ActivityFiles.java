package kr.co.vitamin.repository.vo;

public class ActivityFiles {
	private int atFileNo;
	private int itemNo;
	private String atFileName;
	private String atFileOrigin;
	private String atFilePath;
	
	public int getAtFileNo() {return atFileNo;}
	public void setAtFileNo(int atFileNo) {this.atFileNo = atFileNo;}
	
	public int getItemNo() {return itemNo;}
	public void setItemNo(int itemNo) {this.itemNo = itemNo;}
	
	public String getAtFileName() {return atFileName;}
	public void setAtFileName(String atFileName) {this.atFileName = atFileName;}
	
	public String getAtFileOrigin() {return atFileOrigin;}
	public void setAtFileOrigin(String atFileOrigin) {this.atFileOrigin = atFileOrigin;}
	
	public String getAtFilePath() {return atFilePath;}
	public void setAtFilePath(String atFilePath) {this.atFilePath = atFilePath;}
	
}
